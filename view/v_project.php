	<h4>
		Terrafunder
	</h4>
	<p>
		Terrafunder est un plugin développer pour le jeu Minecraft, version 1.8.*.<br>
		Ce plugin permet de fournir un mode de jeu supplémentaire aux joueurs, ce mode inspiré de SkyDefender.<br>
		<br>
		Indoduction : Deux camps se battant pour protéger et voler le Graal, accéder la victoire en accomplissant votre devoir.<br>
		<br>
		Camps des Attaquants : Sous formes de plusieurs équipes concurranteq, soyez les premiers à voler le Graal et éliminé vos adversaires.<br>
		Camps des Défenseurs : Sous forme d'une seule équipe, protéger le Graal, fortifier votre place forte, et éliminé toutes les équipes attaquantes.<br>
		<br>
		But des Attaquants : Récupérer le bloc d'émeraude qui se situe chez les Défenseurs et le placer sur le bloc de bedrock.<br>
		But des Défenseurs : Protéger le bloc d'émeraude de des équipes Attaquantes.<br>
	</p>
